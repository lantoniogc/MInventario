    private void jButtonSiguienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSiguienteActionPerformed
        
        int unidadTiempoSeleccionada = 1;
        
        if (jRadioButtonDia.isSelected()){
            unidadTiempoSeleccionada = 1;
        }
        if (jRadioButtonSemana.isSelected()){
            unidadTiempoSeleccionada = 2;
        }
        if (jRadioButtonMes.isSelected()){
            unidadTiempoSeleccionada = 3;
        }
        if (jRadioButtonTrimestre.isSelected()){
            unidadTiempoSeleccionada = 4;
        }
        if (jRadioButtonSemestre.isSelected()){
            unidadTiempoSeleccionada = 5;
        }
        
        if (jRadioButtonAnual.isSelected()){
            unidadTiempoSeleccionada = 6;
        }
        
        inventario = new InventarioBase(
                Integer.parseInt(jTextFieldInventarioInicial.getText()),
                unidadTiempoSeleccionada,
                Integer.parseInt(jTextFieldTiempoSimulacion.getText()),
                Integer.parseInt(jTextFieldCostoInventario.getText()),
                Integer.parseInt(jTextFieldCostoOrdenar.getText()),
                Integer.parseInt(jTextFieldCostoCompra.getText()),
                Integer.parseInt(jTextFieldCostoConEspera.getText()),
                Integer.parseInt(jTextFieldCostoSinEspera.getText())
        );
        
        inventario.calcularFactorTiempo();
        inventario.calculaCostoInventarioUnitario();
        
        this.setVisible(false);
        VistaTablasEntrada tablas = new VistaTablasEntrada(
                inventario,
                demanda,
                entrega,
                espera,
                Integer.parseInt(jTextFieldCantidadDemanda.getText()),
                Integer.parseInt(jTextFieldCantidadEntrega.getText()),
                Integer.parseInt(jTextFieldCantidadEspera.getText())
        );
        tablas.generarFilas();
        if (demanda != null && entrega != null && espera != null
            && !demanda.isEmpty()&& !entrega.isEmpty() && !espera.isEmpty()){
            tablas.llenarTablas();
        }
        tablas.setVisible(true);
        tablas.setLocationRelativeTo(null);
        tablas.setSize(850, 625);
        tablas.setTitle("MInventario - Tablas de datos - Analisis");
        tablas.setVisible(true);
        this.dispose();
        
    }//GEN-LAST:event_jButtonSiguienteActionPerformed

