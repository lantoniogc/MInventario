    public void llenarCampos(){
        jTextFieldInventarioInicial.setText("" + inventario.getInventarioInicial());
        switch(inventario.getUnidadDeTiempo()){
            case 1:
                jRadioButtonDia.setSelected(true);
                break;
            case 2:
                jRadioButtonSemana.setSelected(true);
                break;
            case 3:
                jRadioButtonMes.setSelected(true);
                break;
            case 4:
                jRadioButtonTrimestre.setSelected(true);
                break;
            case 5:
                jRadioButtonSemestre.setSelected(true);
                break;
            case 6:
                jRadioButtonAnual.setSelected(true);
                break;                
        }
        jTextFieldTiempoSimulacion.setText("" + inventario.getTiempoSimulacion());
        jTextFieldCostoInventario.setText("" + inventario.getCostoInventario());
        jTextFieldCostoOrdenar.setText("" + inventario.getCostoOrdernar());
        jTextFieldCostoCompra.setText("" + inventario.getCostoCompra());
        jTextFieldCostoConEspera.setText("" + inventario.getCostoFaltanteConEspera());
        jTextFieldCostoSinEspera.setText("" + inventario.getCostoFaltanteSinEspera());
        jTextFieldCantidadDemanda.setText("" + inventario.getCantidadDemanda());
        jTextFieldCantidadEntrega.setText("" + inventario.getCantidadEntrega());
        jTextFieldCantidadEspera.setText("" + inventario.getCantidadEspera());
    }

