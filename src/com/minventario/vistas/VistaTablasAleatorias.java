    public void generarFilas() {
        DefaultTableModel modelDemanda = (DefaultTableModel) jTableDemanda.getModel();
        DefaultTableModel modelEntrega = (DefaultTableModel) jTableEntrega.getModel();
        DefaultTableModel modelEspera = (DefaultTableModel) jTableEspera.getModel();
       
        for (int i=0;i<cantidadDemandasAleatorias;i++) {
            modelDemanda.addRow(new Object[]{0});
        }
        for (int i=0;i<cantidadEntregasAleatorias;i++) {
            modelEntrega.addRow(new Object[]{0});
        }
        for (int i=0;i<cantidadEsperasAleatorias;i++) {
            modelEspera.addRow(new Object[]{0});
        }
    }
        
