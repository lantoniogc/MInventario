    private void jButtonGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonGuardarActionPerformed
        for (int i=0; i<jTableDemanda.getRowCount(); i++){
            vistaTablasEntrada.getDemandaNumerosAleatorios().add(
                    Integer.parseInt(jTableDemanda.getValueAt(i, 0).toString())
            );
        }
        
        for (int i=0; i<jTableEntrega.getRowCount(); i++){
            vistaTablasEntrada.getEntregaNumerosAleatorios().add(
                    Integer.parseInt(jTableEntrega.getValueAt(i, 0).toString())
            );
        }
                
        for (int i=0; i<jTableEspera.getRowCount(); i++){
            vistaTablasEntrada.getEsperaNumerosAleatorios().add(
                    Integer.parseInt(jTableEspera.getValueAt(i, 0).toString())
            );
        }
        
        this.dispose();
    }//GEN-LAST:event_jButtonGuardarActionPerformed

